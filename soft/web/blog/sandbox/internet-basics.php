			<span style="margin-left:60px;"></span>On one particularly fine afternoon, Ethel was lounging around the house when she decided to pull out her Sears catalogue to buy the fancy new electric radio she’d been eyeing. Little did she know, she simultaneously became the subject of a beautiful extended metaphor. <br><br>

			<span style="margin-left:60px;"></span>Ethel filled out and mailed her order form, <i>violently</i> anxious for her delivery. The mail man collected her letter, giving Ethel a knowing nod as he drove away, and that letter made it’s way to the Sears headquarters in Chicago for processing. A worker read the order, walked around the Sears warehouse to gather the necessaries, and prepared the product for shipping.<br><br>


			<i>Ethel is not real.</i> <br><br>

			<span style="margin-left:60px;"></span>Ethel is an <b>internet browser,</b> Sears is a <b>server,</b> the product catalogue is a <b>search engine,</b> and the postal service is the system of <b>modems, routers,</b> and so on that connect you to a server. <br><br>

			<span style="margin-left:60px;"></span>Everything that Ethel did is called <b>“client side,”</b> and everything that Sears did is called <b>“server side.”</b> Client side “scripts” are instructions that your browser completes (often written with the language <b>JavaScript</b>), and server side scripts are instructions that the server you're communicating with completes (often written with the language <b>PHP</b>). <br><br>

			<span style="margin-left:60px;"></span>Your computer and the servers <b>‘hosting’</b> a website you access communicate in very basic text files, which are merely descriptions of what the browser should draw on the screen; along the lines of “make a big red box that says ‘hello world’ inside of it.” These visual instructions are written with <b>HyperText Markup Language (HTML)</b> which is structure, and <b>Cascading Style Sheets (CSS)</b>, which is the styling of that structure.<br><br>

			<span style="margin-left:60px;"></span>In summation:<br><br>

			<span style="margin-left:60px;"></span><b>HTML:</b> The structure of anything on the web. It’s a description of what a page should look like.<br><br>
			<span style="margin-left:60px;"></span><b>CSS:</b> The style of the structure, works in conjunction with HTML.<br><br>
			<span style="margin-left:60px;"></span><b>PHP:</b> A server side scripting language, used to pull information from a database, process forms, anything on the server.<br><br>
			<span style="margin-left:60px;"></span><b>JavaScript:</b> A client side scripting language, executed by an internet browser to do things like animate onscreen elements, define what happens when a button is pressed, etc.<br><br>
			<span style="margin-left:60px;"></span><b>Server:</b> A computer that stores files and sends them to your browser when requested, as well as execute server side scripts.<br><br>
			<span style="margin-left:60px;"></span><b>Browser:</b> A program that decodes HTML/CSS and executes other instructions from client side scripts.<br><br>

